                                <h3>Simple ou double côtes ?</h3>
                                <div class="well">
                                    <h4>Les doubles côtes</h4>
                                    <hr>
                                    <p>Il y a une différence majeur entre ces 2 utilisations. <strong>Les doubles côtes permettent elles d'afficher du texte à l'écran mais d'également saisir/lire une variable.</strong> Il est dit que pour php, l'utilisation de double côtes, ralenti le système sachant qu'il fait une vérification supplémentaire afin de savoir si une variable existe ou non.</p>
                                    <div class="bg-info">
                                        <p>Il faut savoir que, les variables saisi entre double côtes n'auront pas de colorisation syntaxique !</p>
                                    </div>
                                    <pre>
        <span class="balisephp">&lt;?php</span>
            <span class="variable">$myString3</span> = <span class="simpleCote">'Alain'</span>;
            <span class="function">echo</span> <span class="simpleCote">"Je m'appel $myString3"</span>; 
        <span class="balisephp">?&gt;</span>
                                    </pre>
                                    <div class="bg-warning">
                                        <?php
                                            $myString3 = 'Alain';
                                            echo "Je m'appel $myString3";
                                        ?>
                                    </div> 
                                </div><!-- Fin des doubles côtes -->
                                <div class="well">
                                    <h4>Les simple côtes</h4>
                                    <hr>
                                    <p>Les simple côtes permettent de déceler très facillement une variable dans un chaîne de caractère grâce à la concaténation.</p>
                                    <div class="bg-danger">
                                        <p>Attention tout de même, si on utilise exactement des variables au sein même d'une chaîne de caractère entouré de simple côte, la variable ne sera jamais interprêté.</p>
