                                <div class="well">
                                    <p>En JavaScript, on utilise le + pour concaténer deux chaînes de caractères entre elles. En php, ce n'est pas le + mais <strong>LE POINT </strong> qui permet de faire exactement la même chose</p>
                                    <pre>
        <span class="balisephp">&lt;?php</span>
            <span class="variable">$myString2</span> = <span class="simpleCote">'Alain'</span>;
