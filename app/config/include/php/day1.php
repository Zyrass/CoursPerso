                                        <h2>Qu'es-ce-que PHP ?</h2>
                                        <div class="well">
                                            <p>PHP, initial de <strong>Hypertext Preprocessor</strong> est un langage informatique créé en 1994 par <a href="https://fr.wikipedia.org/wiki/Rasmus_Lerdorf" target="_blank" title="auteur de php">Rasmus Lerdorf</a>. Aussi appelé programmation procédurale, elle est utilisée aujourd'hui par énormément de monde, il permet de développer des sites dynamique. Il faut savoir que les <abbr title="Content Management System">CMS</abbr> utilisent pour la plus part le PHP.<br />
                                            ( Si vous envisagez d'utiliser par la suite une plateform comme wordpress par exemple, vous devrez indéniablement apprendre PHP afin de pouvoir profiter pleinement de ce système).</p>
                                            <hr>
                                            <p>Il faut savoir que tous les navigateurs sont incapable de décoder du php comme ils le font avec du html/css ou javascript. Le php est un langage de type serveur. Pour interprêter localement du php, il faut disposer d'un serveur apache qui transformera le php en HTML selon ce que l'on aura saisit.</p>
                                            <div class="well text-center">
                                                <h3>Un shéma représentatif du langage php</h3>
                                                <figure>
                                                    <figcaption>Exemple parfait du fonctionnement de PHP</figcaption>
                                                    <img src="https://www.alsacreations.com/xmedia/doc/original/schema-http.png" alt="fonctionnement de php">
                                                </figure>
                                            </div>
                                        </div>

                                        <h2>Pourquoi l'utiliser ?</h2>
                                        <div class="well">
                                            <p>PHP est de loin le langage le plus utilisé sur internet. Aujourd'hui il en est à sa version 7.1. <em>La dernière version fait polémique.. Par contre la version 7.0.12 fonctionne très bien. (Utilisation personnel)</em>.<br /><br />
                                            Donc, pourquoi utiliser le PHP ? <span class="text-info">Imaginez un instant la conception d'un site avec 69 pages.. Imaginez que vous voulez modifier un élément du menu principal...</span></p>
                                            <blockquote>
                                                <p><strong>En html classique, vous serez obligé d'éditer 69 fois la même chose...<br />
                                                Franchement, vous êtes vraiment un grand malade !!</strong></p>
                                            </blockquote>
                                            <p>En php, il existe une possibilitée d'extraire une portion de code pour réutiliser cette même portion dans un autre fichier. Également et ça c'est obligatoire, pour taiter un formulaire, vous serez obligé de passer par PHP afin de gérer le traitement que ce soit au niveau de la sécuritée ou autre.<br /><br />
                                            <span class="text-danger">N'oubliez pas, ne faîtes jamais confiance à qui que ce soit. Contrôler chaque champs qu'un utilisateur rempli peut vous éviter de perdre des heures et des heures pour rien. Pensez à sauvegarder régulièrement vos données.</span></p>
                                        </div>

                                        <h2>Avantage de la programmation procédural</h2>
                                        <div class="well">
                                            <p>Le plus grand des avantages de la programmation procédurale, c'est que nous sommes maître à bord sur, tout ce qui sera affiché à l'écran. Ainsi si les connaissances sont là, <abbr title="Imaginon la conception d'un code très complexe utilisé à des fins malveillant. Renommé ce fichier en photo.img.. Et là c'est le drame, le code sera interprêté sans qu'on puisse le voir... Et à ce moment se sera trop tard.">on peut par exemple, éviter d'injecter du code malicieux en sécurisant tout ça.</abbr><br /><br />Récupérer des informations sur la provenance d'une photo. Par exemple, savoir avec quel <abbr title="Appareil photo numérique">APN</abbr> une quelconque photo a été prise.<br /><br />Nous pouvons aussi via des expressions régulière spécifier un certain type de donnée qu'un utilisateur pourra saisir. <strong>Pour être franc, la liste est tellement longue que le net regorge d'idée. Je vous invite à être curieux, sachez que c'est une très belle qualitée.</strong></p>
                                        </div>

                                        <h2>Ce qu'il faut savoir avant</h2>
                                        <div class="well">
                                            <p>Ici, je vous explose à mon sens les langages qu'il faudrait connaître pour se sentir à l'aise dans l'apprentissage du php.</p>
                                            <div class="alert alert-info">
                                                <p><strong>Attention, tout ce qui suit n'engage que moi.</strong><br /><br />
                                                Vous êtes libre de commencer par le php mais très franchement vous allez plus galérer qu'autre chose.</p>
                                            </div>
                                            <ol>
                                                <li><mark>HTML 5</mark></li>
                                                    <p>Pour tout développeur quel qu'il soit, le html reste la base et sa connaissance est pour moi plus qu'indispensable.</p>
                                                <li><mark>CSS 3</mark></li>
                                                    <p>Si vous apprenez l'html, forcément vous allez passé par le CSS. Alors ce langage n'est pas forcément indispensable mais, si vous voulez avoir une page tout de même potable, il vous faudra au moins connaître la base.</p>
                                                <li><mark>JavaScript</mark></li>
                                                    <p>A quelques différences prêt, le javascript nous permettra de gagner du temps. En effet certaines ligne de code sont utilisés sur php. Ainsi si vous avez une petite base vous avancerez un tout petit peu plus vite qu'une personne qui n'y connait rien.</p>
                                                <li><mark>Les Framework tel que Bootstrap</mark></li>
                                                    <p>Pour gagner énormément de temps, il serait préférable de connaître un minimum les syntaxes de base. (Bon il y a la doc mais switcher entre la doc et notre code toutes les 30 secondes c'est vite casse bonbon). </p>
                                                <li><mark>Les Frameword PHP</mark></li>
                                                    <p>Formellement déconseiller. <strong>On va apprendre la programmation procédural</strong>. Les framework php eux utilisent <strong>la programmation orienté objet (POO)</strong>. Il faut pour comprendre la POO, connaître <strong>impérativement</strong> la programation procédural.</p>
                                            </ol>
                                        </div>

