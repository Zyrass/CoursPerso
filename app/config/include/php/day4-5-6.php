                        <h3>Scope Static</h3>
                        <div class="well">
                            <!-- Scope static -->
                            <p>Le scope static est utiliser pour fixé une valeur particulière. Ainsi a chaque appel de la fonction, la valeur restera inchangé. Par contre si vous incrémenté un nombre, à chaque fois que vous allez appelé cette fonction, la dernière incrémentation sera gardé en mémoire. Un exemple sera certainement plus parlant.</p>
                            <pre>
        <span class="commentaire">// Utilisation du mot clé static dans notre fonction.</span>
        <span class="function">function</span> <span class="functionName">test7()</span> {
            <span class="instruction">static</span> <span class="variable">$myNumber</span> = <span class="integer">50</span>;
            <span class="variable">$myNumber</span>++;
            <span class="instruction">echo</span> <span class="variable">$myNumber</span>;
        }
        
        <span class="commentaire">// Ici j'appel 4 fois la fonction afin de vous montrez qu'on a bien garder en mémoire l'information</span>
        <span class="functionName">test7()</span>;
        <span class="functionName">test7()</span>;
        <span class="functionName">test7()</span>;
        <span class="functionName">test7()</span>;
                            </pre>
                            <div class="bg-success">
                                <?php
                                function test7() {
                                    static $myNumber = 50;
                                    $myNumber++;
                                    echo $myNumber . " ";
                                }

                                test7();
                                test7();
                                test7();
                                test7();
                                ?>
                            </div>
                            <br>
                            <!-- Même exemple d'un scope static sans le mot clé static -->
                            <p>Reprenons le même exemple sans le mot clé static afin de voir la différence.</p>
                            <pre>
        <span class="commentaire">// Retrait du mot clé static dans notre fonction.</span>
        <span class="function">function</span> <span class="functionName">test8()</span> {
