            <span class="variable">$myNumber</span>++;
            <span class="instruction">echo</span> <span class="variable">$myNumber</span>;
        }

        <span class="commentaire">// Ici j'appel à nouveau 4 fois la fonction. Constatez le changement.</span>
        <span class="functionName">test8()</span>;
        <span class="functionName">test8()</span>;
        <span class="functionName">test8()</span>;
        <span class="functionName">test8()</span>;
                            </pre>
                            <div class="bg-danger">
                                <?php
                                function test8() {
                                    $myNumber = 50;
                                    $myNumber++;
                                    echo $myNumber . " ";
                                }

                                test8();
                                test8();
                                test8();
                                test8();
                                ?>
