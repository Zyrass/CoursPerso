                        <h3>Scope globale</h3>
                        <div class="well">
                            <!-- Variable globale -->
                            <p>Une variable global peut être lu sur toute la page, mais pas dans une fonction à 2 exceptions prêt.</p>
                            <pre>
        <span class="variable">$variableGlobal1</span> = <span class="simpleCote">'Je suis une variable global'</span>;
        
        <span class="commentaire">// Conception d'une fonction pour montrer le fonctionnement d'une variable globale.</span>
        <span class="function">function</span> <span class="functionName">test()</span> {
            <span class="instruction">echo</span> <span class="simpleCote">'test'</span>;
        }
        
        <span class="commentaire">// Cette variable est de type globale vu qu'elle est accessible partout dans le code.</span>
        <span class="instruction">echo</span> <span class="variable">$variableGlobal1</span>;
                            </pre>
                            <div class="bg-warning">
                                <?php
                                    $variableGlobal1 = 'Je suis une variable global';
                                    
                                    function test() {
                                        echo 'test';
                                    }
                                
                                echo $variableGlobal1;
                                ?>
                            </div>
                            <br />
                            <!-- Variable dans une fonction -->
                            <p>Maintenant la même variable mais cette fois définit dans une fonction.</p>
                            <pre>
        <span class="variable">$variableGlobal2</span> = <span class="simpleCote">'Je suis une variable global dans une fonction'</span>;

        <span class="commentaire">// Utilisation de notre variable dite global dans notre fonction.</span>
        <span class="function">function</span> <span class="functionName">test2()</span> {
            <span class="instruction">echo</span> <span class="variable">$variableGlobal2</span>;
        }

        <span class="commentaire">// Initialisation de la variable.</span>
        <span class="functionName">test2()</span>;
                            </pre>
                            <div class="bg-danger">
                                <?php
                                $variableGlobal2 = 'Je suis une variable global dans une fonction';

                                function test2() {
                                    echo $variableGlobal2;
                                }
                                
                                test2();                                
                                ?>
                            </div>
                            <br />
                            <!-- Avec le mot clé global -->
                            <p>Utilisons le mot clé globale pour faire fonctionner notre fonction.</p>
                            <pre>
