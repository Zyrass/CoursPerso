                        <h3>La boucle while</h3>
                        <div class="well">
                            <p>While qui veut dire <strong>tant que</strong>, permet de faire une boucle selon une condition spécifique.</p>
                            <div class="alert alert-danger">
                                <p>Le problème d'une boucle while c'est que si elle est mal conçue, on va se retrouver avec une boucle infini.</p>
                            </div>
                            <pre>
        <span class="variable">$initWhile</span> = <span class="commentaire">valeur</span>;
        <span class="instruction">while (</span> <span class="commentaire">condition avec la variable initialisée</span> <span class="instruction">)</span> {
            <span class="instruction">echo</span> <span class="commentaire">la variable initialisée incrémentée;</span>
        }
                            </pre>
                            <pre>
        <span class="variable">$initWhile</span> = <span class="integer">1</span>;
        <span class="instruction">while (</span> <span class="variable">$initWhile</span> &lt; <span class="integer">5</span> <span class="instruction">)</span> {
            <span class="instruction">echo</span> <span class="variable">$initWhile</span>++;
        }
                            </pre>
                            <div class="alert alert-info">
                                <p>A savoir : La variable <strong>$initWhile</strong> est généralement appelé sur la toile <strong>$i</strong> pour incrémentation.</p>
                            </div>
                            <div class="bg-warning">
                                <?php
                                    $initWhile = 1;
                                    while ($initWhile <= 5) {
                                        echo $initWhile++ . ' ';
                                    }
                                ?>
                            </div>
                        </div>
                        <h3>Autre syntaxe</h3>
                        <div class="well">
                            <div class="alert alert-info">
                                <p>Si vous êtes curieux, vous pouvez vous renseigner sur les syntaxes alternative. Pour celà vous devrez vous rendre sur la documentation officiel.</p>
                                <p>Allez je suis gentil, voici la page en question : <a href="http://php.net/manual/fr/control-structures.alternative-syntax.php" target="_blank">Syntaxe alternative</a></p>
                            </div>
                        </div>
                        <h3>La boucle Do While</h3>
                        <div class="well">
                            <div class="alert alert-info">
                                <p>La boucle DO WHILE est exactement comme le WHILE. <strong>La différence qu'il ne faut surtout pas négliger, c'est que cette boucle avant de démarrer, elle affichera à l'écran le tout premier tour de boucle avant de démarrer la boucle.</strong> A savoir qu'elle est pratiquement pas utilisé.</p>
                            </div>
                            <pre>
        <span class="variable">$i</span> = <span class="commentaire">valeur</span>;
        <span class="instruction">do</span> {
            <span class="instruction">echo</span> <span class="variable">$i</span>;
        } <span class="instruction">while (</span> <span class="commentaire">condition</span> <span class="instruction">)</span>;
                            </pre>
                            <pre>
        <span class="variable">$i</span> = <span class="integer">100</span>;
        <span class="instruction">do</span> {
            <span class="instruction">echo</span> <span class="variable">$i</span>;
        } <span class="instruction">while (</span> <span class="variable">$i</span> &gt; 105 <span class="instruction">)</span>;
                            </pre>
                            <div class="bg-warning">
                                <?php
                                $i = 100;
                                do {
                                    echo $i++ . ' ';
                                } while ($i < 150);
                                ?>
                            </div>
                        </div>
                        <h3>La boucle for</h3>
                        <div class="well">
                            <p>La boucle for permet de faire sensiblement la même chose que la boucle while en revanche le tout tient sur une seule ligne.</p>
                            <pre>
        <span class="instruction">for (</span> <span class="commentaire">initialisation</span>; <span class="commentaire">condition</span>; <span class="commentaire">incrémentation</span> <span class="instruction">)</span> {
            <span class="instruction">echo</span> <span class="commentaire">message à répété avec la variable $i</span>;
        }
                            </pre>
                            <pre>
        <span class="instruction">for (</span> <span class="variable">$i</span> = <span class="integer">0</span>; <span class="variable">$i</span> &lt; <span class="integer">5</span>; <span class="variable">$i</span>++ <span class="instruction">)</span> {
            <span class="instruction">echo</span> <span class="simpleCote">'Je suis la ligne numéro : ' . $i . '&lt;br /&gt;'</span>;
        }
                            </pre>
                            <div class="bg-warning">
                                <?php
                                    for ( $i = 0; $i < 5; $i++ ) {
                                        echo 'Je suis la ligne numéro : ' . $i . '<br />';
                                    }
                                ?>
                            </div>
