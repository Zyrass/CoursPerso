        }
        
        <span class="commentaire">// Initialisation de la fonction : <strong>salutLa3wa()</strong></span>
        <span class="functionName">salutLa3wa()</span>;
                            </pre>
                            <div class="bg-warning">
                                <?php
                                    function salutLa3wa() {
                                        echo "Je compte devenir le futur Mark Zuckerberg.. Bon ok... on ne s'enflamme pas hein !!";
