                        <h3>Quand l'utiliser ?</h3>
                        <div class="well">
                            <div class="alert alert-info">
                                <p>Lorsque vous aurez fini votre site, <a href="http://www.geek-directeur-technique.com/2009/08/26/la-refactorisation" title="Lien vers une très belle explication sur le refactoring">un refactoring</a> s'impose. Il faut savoir que quand vous avez fini de coder votre page, il serait préférable de la refactoriser. Si votre site contient 200 page, refactoriser 200 pages vous prendrais des heures... alors que si vous faîtes le point intelligemment à la fin d'une page, il vous restera que quelques petites choses à revoir.</p>
                                
                                <p>Une phrase que j'aime beaucoup : <strong>La refactorisation est un exercice qui devrait être maîtrisé par tous les développeurs, encadré par tous les chefs de projets et encouragé par tous les directeurs techniques.</strong></p>
                                
                                <p>Une fonction une fois qu'elle aura été déclarer, si on réécrit exactement la même syntaxe, on se retrouve avec une jolie erreur fatal.</p>
